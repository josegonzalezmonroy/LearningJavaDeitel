public class ElseOscilante2 
{
    public static void main(String[] args) 
    {
        int x = 3;
        int y = 8;

        if (y == 8)
        {
            System.out.println("@@@@@y");
        }
        else if (x == 5)
        {

            System.out.println("@@@@@x");
        
        }
        else
        {
            System.out.println("#####");
            System.out.println("$$$$$");   
            System.out.println("&&&&&");   
        }
    }
}
